/*===========================
该文件定义了方块类，涉及方块的属性和操作
方块坐标posX和posY表示方块数组左上角的坐标
有关方块移动和变形的函数在player.h
关于坐标的不同：
方块类的坐标与渲染类的函数需要传入的坐标不同
详情见render.h
=============================*/
#pragma once
class Block {
public:
	Block();//构造函数
	Block(const Block&);//复制构造函数
	void setX(int x);//设置方块坐标
	void setY(int y);
	void setShape(int s);//设置方块形状
	void roundBlock();//随机生成方块
	int getX();//获取方块坐标
	int getY();
	int getColor();//获取方块颜色
	int getShape();//获取方块形状
private:
	int posX;//方块在某玩家地图上的坐标
	int posY;//x为行,y为列
	int color;//方块颜色
	int shape;//方块形状
	friend class Player; //玩家类为友元类
};
